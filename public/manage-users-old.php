<?php
require_once __DIR__ . '/../app/Auth.php';
require_once __DIR__ . '/../app/Models/User.php';

Auth::requireAdmin();

$userModel = new User();
$users = $userModel->getAll();

// Handle form submissions
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    if (isset($_POST['action'])) {
        switch ($_POST['action']) {
            case 'create':
                $result = $userModel->create([
                    'name' => $_POST['name'],
                    'email' => $_POST['email'],
                    'password' => $_POST['password'],
                    'role' => $_POST['role']
                ]);
                $message = $result ? 'User created successfully!' : 'Error creating user.';
                break;
                
            case 'update':
                $result = $userModel->update($_POST['id'], [
                    'name' => $_POST['name'],
                    'email' => $_POST['email'],
                    'role' => $_POST['role']
                ]);
                $message = $result ? 'User updated successfully!' : 'Error updating user.';
                break;
                
            case 'delete':
                $result = $userModel->delete($_POST['id']);
                $message = $result ? 'User deleted successfully!' : 'Error deleting user.';
                break;
        }
        
        // Refresh user list
        $users = $userModel->getAll();
    }
}
